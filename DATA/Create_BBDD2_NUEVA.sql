CREATE DATABASE RAW_DATA; 

USE RAW_DATA;

CREATE TABLE EMPLEADO (
   EMPLOYEE_NUMBER INT PRIMARY KEY,
   AGE INT,
   EDUCATION INT,
   EDUCATION_FIELD VARCHAR(100),
   GENDER VARCHAR(50),
   MARITAL_STATUS VARCHAR(50),
   WORK_LIFE_BALANCE INT,
   NUM_COMPANIES_WORKED INT,
   RELATIONSHIP_SATISFACTION INT,
   TOTAL_WORKING_YEARS INT
);



CREATE TABLE REGISTRO (
   ID_REGISTRO INT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
   EMPLOYEE_NUMBER INT NOT NULL,
   DISTANCE_FROM_HOME INT,
   REMOTE_WORK BOOLEAN,
   MONTHLY_INCOME INT,
   HOURLY_RATE INT,
   OVER_TIME BOOLEAN,
   ENVIRONMENT_SATISFACTION INT,
   FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLEADO(EMPLOYEE_NUMBER)
);


CREATE TABLE EMPLEADO_EMPRESA (
    EMPLOYEE_NUMBER INT PRIMARY KEY,
    DEPARTMENT VARCHAR(100),
    BUSINESS_TRAVEL VARCHAR(100),
    JOB_INVOLVEMENT INT,
    JOB_ROLE VARCHAR(100),
    JOB_SATISFACTION INT,
    ATTRITION BOOLEAN,
    YEARS_AT_COMPANY INT, 
    MONTHLY_RATE INT,
    PERCENT_SALARY_HIKE INT,
    STOCK_OPTION_LEVEL INT, 
    TRAINING_TIMES_LAST_YEAR INT, 
    YEARS_SINCE_LAST_PROMOTION INT, 
    YEARS_WITH_CURRENT_MANAGER INT,
    DAILY_RATE INT,
    PERFORMANCE_RATING INT,
    FOREIGN KEY (EMPLOYEE_NUMBER) REFERENCES EMPLEADO(EMPLOYEE_NUMBER)
);


